const axios = require('axios');
/**
 * 
 * @param {*} url - base url
 * @param {*} token - Bearer token
 */
const HTTP = (url, token) =>
  axios.create(
    {
      baseURL: url,
      headers: {
        Authorization: `Bearer ${token}` // The token is generated by a hook
      }
    }
  );

module.exports = HTTP;